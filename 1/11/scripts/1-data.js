var User = function (
	id,
	name,
	surname
) {
	this.id = id;
	this.name = name;
	this.surname = surname;
	this.$row = $('<tr>');

	/**
	 * This method check if value of object is equal to given value.
	 *
	 * @param {String} field
	 * @param {String} value
	 * @returns {Boolean}
	 */
	this.isEqual = function (field, value) {
		return value === this[field];
	};

	this.isLowerThan = function (field, value) {
		return value > this[field];
	};

	this.isHigherThan = function (field, value) {
		return value < this[field];
	};

	this.containsValue = function (field, value) {
		return -1 !== this[field].toLowerCase().indexOf(value.toLowerCase());
	};

	this.populate$row = function () {
		this.$row.append('<td>' + this.id + '</td>');
		this.$row.append('<td>' + this.name + '</td>');
		this.$row.append('<td>' + this.surname + '</td>');
	};

	this.get$row = function () {
		return this.$row;
	};

	this.populate$row();
};

